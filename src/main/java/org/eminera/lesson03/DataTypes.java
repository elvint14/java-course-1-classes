package org.eminera.lesson03;

public class DataTypes {
  public static void main(String[] args) {
    // variables

    // qaliqsiz (Integer types) => tam ededler
    byte ageB = 1;
    short ageS = -2;
    int ageI = 0;
    long ageL = 12;

    // qaliqli (Floating-point types) => heqiqi ededler
    float f = 5.4f;
    double d = 5.4d;

    // mentiqi (Boolean)
    boolean b1 = false;
    boolean b2 = true;

    // Character type
    char c1 = 'c';
    char c2 = '1';
    char c3 = '$';
    char c4 = '\u00A9';

    System.out.println(c1);
    System.out.println(c4);

    char abcd = 'a';
    System.out.println("-------");
    System.out.println((int) abcd);
    System.out.println("-------");

    // bu bir comment-dir - line comment

    /* block comment
    refs
    sdfdsf
    asdsadf

    asdasd
    asdasd

     */

    /**
     * development uchun
     */

  }
}
